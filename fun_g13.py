        while True:  # –í–Ω—É—Ç—Ä–µ–Ω–Ω–∏–π —Ü–∏–∫–ª
            if y == 1:  # –£–º–Ω–æ–∂–µ–Ω–∏–µ –Ω–∞ 1 –ø—Ä–æ–ø—É—Å–∫–∞–µ–º
                y += 1  # –£–≤–µ–ª–∏—á–∏–≤–∞–µ–º —Å—á–µ—Ç—á–∏–∫ –≤–Ω—É—Ç—Ä–µ–Ω–Ω–µ–≥–æ —Ü–∏–∫–ª–∞ –Ω–∞ –µ–¥–∏–Ω–∏—Ü—É
