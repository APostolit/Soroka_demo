# Строка в одинарных кавычках
str_1 = 'Жил старик со своею старухой'
print(str_1)

# Строка в двойных кавычках
str_2 = "У самого синего моря."
print(str_2)

# Используем три двойные кавычки
str_3 = """Они жили в ветхой землянке
Ровно тридцать лет и три года."""
print(str_3)

# Используем три апострофа
str_4 = '''Старик ловил неводом рыбу,
Старуха пряла свою пряжу.'''
print(str_4)