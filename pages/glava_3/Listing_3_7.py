# Рост героев
Gena = 160       # Это рост крокодила Гены
Cheburaska = 40  # Это рост Чебурашки
Shapoklak = 160  # Это рост старухи Шапокляк

# Сравнение рост героев
print('Гена и Шапокляк одного роста:', Gena == Shapoklak)
print('Гена и Чебурашка одного роста:', Gena == Cheburaska)
print('Гена выше Чебурашки:', Gena > Cheburaska)
print('Гена ниже Чебурашки:', Gena < Cheburaska)
print('Гена и Шапокляк разного роста:', Gena != Shapoklak)
print('Гена и Чебурашка разного роста:', Gena != Cheburaska)