# Листинг 3.3
x = 10.5
y = 2.2
print('Даны числа x=', x, ' y=', y)

# Сложение
z = x + y
print('Сложение:', z)

# Вычитание
z = x - y
print('Вычитание:', z)

# Умножение
z = x * y
print('Умножение:', z)

# Деление
z = x / y
print('Деление:', z)

# Возведение в степень
z = x ** y
print('Возведение в степень:', z)