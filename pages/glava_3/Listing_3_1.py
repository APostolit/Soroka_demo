# Листинг 3.1
# Целое положительное число.
int_1 = 15
print('int_1=', int_1)

# Целое отрицательное число
int_2 = -2
print('int_2=', int_2)

# Сложение целых чисел
r = int_1 + int_2
print('Сложение r=', r)

# Вычитание целых чисел
r = int_1 - int_2
print('Вычитание r=', r)

# Умножение целых чисел
r = int_1 * 2
print('Умножение r=', r)

# Деление целых чисел
r = int_1 / 2
print('Деление r=', r)