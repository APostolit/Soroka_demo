import streamlit as st

def run_10_1():
    st.markdown('#### üêç –£—Ä–æ–∫ 10.1. –ò–Ω—Å—Ç—Ä—É–∫—Ü–∏–∏ –≤ Python')
    st.markdown('##### –ò–Ω—Å—Ç—Ä—É–∫—Ü–∏—è - —ç—Ç–æ –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç—å –¥–µ–π—Å—Ç–≤–∏–π')
    from math import pi
    # –ü—Ä–∏—Å–≤–∞–∏–≤–∞–Ω–∏–µ –∑–Ω–∞—á–µ–Ω–∏–π –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–º
    x, y, z = [1, 2, 3]
    # –û–±—ä—è–≤–ª–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏
    def my_func(z=10):
        s_1 = z ** 2
        for i in range(25):  # –ó–∞–ø—É—Å–∫–∞–µ—Ç —Ü–∏–∫–ª
            if i < 5:  # –£—Å–ª–æ–≤–Ω–∞—è –∏–Ω—Å—Ç—Ä—É–∫—Ü–∏—è
                continue  # –ü–µ—Ä–µ—Ö–æ–¥–∞ –≤ –Ω–∞—á–∞–ª–æ —Ü–∏–∫–ª–∞
            elif 5 <= i < 10:
                s_1 += i  # –£–≤–µ–ª–∏—á–∏–≤–∞–µ–º s_1
            else:
                break  # –ü—Ä–µ—Ä—ã–≤–∞–Ω–∏–µ —Ü–∏–∫–ª–∞
        return s_1  # –í–æ–∑–≤—Ä–∞—Ç–∞ –∑–Ω–∞—á–µ–Ω–∏—è —Ñ—É–Ω–∫—Ü–∏–∏
    # –ò–Ω—Å—Ç—Ä—É–∫—Ü–∏—è –±—É–¥–µ—Ç –æ–±—Ä–∞–±–∞—Ç—ã–≤–∞—Ç—å (–æ—à–∏–±–∫–∏)
    try:
        # –ò–Ω—Å—Ç—Ä—É–∫—Ü–∏—è –æ–±—Ä–∞—â–µ–Ω–∏—è –∫ —Ñ—É–Ω–∫—Ü–∏–∏
        a_1 = my_func(z)
        # –í—ã–ø–æ–ª–Ω–µ–Ω–∏–µ –∞—Ä–∏—Ñ–º–µ—Ç–∏—á–µ—Å–∫–∏—Ö –æ–ø–µ—Ä–∞—Ü–∏–π
        res = a_1 + int(pi) * 10 + x + y
        # –ò–Ω—Å—Ç—Ä—É–∫—Ü–∏—è –≤—ã–≤–æ–¥–∏—Ç —Ä–µ–∑—É–ª—å—Ç–∞—Ç
        st.write("–†–µ–∑—É–ª—å—Ç–∞—Ç –≤—ã—á–∏—Å–ª–µ–Ω–∏–π res=", res)
    # –ë–ª–æ–∫ –∏–Ω—Å—Ç—Ä—É–∫—Ü–∏–π –æ–±—Ä–∞–±–æ—Ç–∫–∏ –æ—à–∏–±–æ–∫
    except Exception as err:
        print(err)  # –í—ã–≤–æ–¥–∏–º —Å–æ–æ–±—â–µ–Ω–∏–µ –æ–± –æ—à–∏–±–∫–µ
    else:  # –ï—Å–ª–∏ –æ—à–∏–±–æ–∫ –Ω–µ—Ç
        pass  # –ø—É—Å—Ç–∞—è –∏–Ω—Å—Ç—Ä—É–∫—Ü–∏—è
    finally:  # –≠—Ç–æ—Ç –±–ª–æ–∫ –≤—ã–ø–æ–ª–Ω—è–µ—Ç—Å—è –≤—Å–µ–≥–¥–∞
        st.write('–ö–æ–Ω–µ—Ü –ø—Ä–æ–≥—Ä–∞–º–º—ã')
    st.write('üé• –°–º–æ—Ç—Ä–∏—Ç–µ –º—É–ª—å—Ç—Ñ–∏–ª—å–º: –ø—Ä–æ –∏–Ω—Å—Ç—Ä—É–∫—Ü–∏—é')

