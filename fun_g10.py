def run_10_3():
    st.markdown('#### üêç –£—Ä–æ–∫ 10.3. –û–ø–µ—Ä–∞—Ç–æ—Ä—ã –≤ Python')
    st.markdown('##### –°–∫–æ–±–∫–∏ –º–æ–≥—É—Ç –∏–∑–º–µ–Ω–∏—Ç—å –ø—Ä–∏–æ—Ä–∏—Ç–µ—Ç –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è –æ–ø–µ—Ä–∞—Ü–∏–π')
    st.write("–ü—Ä–∏–æ—Ä–∏—Ç–µ—Ç –æ–ø–µ—Ä–∞—Ç–æ—Ä–∞ '*' –≤—ã—à–µ, —á–µ–º —É '+'")
    a = 6 + 9 * 2
    st.write('6 + 9 * 2 ->', a)
    st.write("–°–∫–æ–±–∫–∏ —Å–º–µ–Ω–∏–ª–∏ –ø—Ä–∏–æ—Ä–∏—Ç–µ—Ç, —Ç–µ–ø–µ—Ä—å '+' –≤—ã—à–µ, —á–µ–º —É '*'")
    b = (6 + 9) * 2
    st.write('(6 + 9) * 2 ->', b)
    st.write('-' * 50)
    c = 2
    st.write('–ü—Ä–∏–æ—Ä–∏—Ç–µ—Ç —Å–ª–µ–≤–∞-> –Ω–∞–ø—Ä–∞–≤–æ.')
    st.write('a < b > c ->', a < b > c)
    st.write("–°–∫–æ–±–∫–∏ —Å–º–µ–Ω–∏–ª–∏ –ø—Ä–∏–æ—Ä–∏—Ç–µ—Ç, —Ç–µ–ø–µ—Ä—å –Ω–∞–ª–µ–≤–æ <- —Å–ø—Ä–∞–≤–∞.")
    st.write('a < (b > c) ->', a < (b > c))
    st.write('-' * 50)
    # –°–æ–≥–ª–∞—Å–Ω–æ –ø—Ä–∏–Ω—è—Ç–æ–º—É –ø–æ—Ä—è–¥–∫—É —ç—Ç–æ —Ç–æ–∂–µ —Å–∞–º–æ–µ, —á—Ç–æ –∏ c = (a = b)
    st.write("–ó–¥–µ—Å—å –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é –ø—Ä–∏–æ—Ä–∏—Ç–µ—Ç –Ω–∞–ª–µ–≤–æ <- —Å–ø—Ä–∞–≤–∞.")
    st.write("–ö–∞–∫ –±—É–¥—Ç–æ –ø—Ä–∏—Å—É—Ç—Å—Ç–≤—É—é—Ç —Å–∫–æ–±–∫–∏ c = (a = b).")
    st.write('b=', b)
    st.write('a=', a)
    c = a = b
    st.write('c = a = b ->', c, a, b)
    st.write('-' * 50)
    st.write('–û–ø–µ—Ä–∞—Ç–æ—Ä—ã "-" –∏ "-" –∏–º–µ—é—Ç —Ä–∞–≤–Ω—ã–π –ø—Ä–∏–æ—Ä–∏—Ç–µ—Ç.')
    st.write('–ü–æ —É–º–æ–ª—á–∞–Ω–∏—é –ª–µ–≤–∞—è –∞—Å—Å–æ—Ü–∏–∞—Ç–∏–≤–Ω–æ—Å—Ç—å.')
    st.write('–ö–∞–∫ –±—É–¥—Ç–æ –ø—Ä–∏—Å—É—Ç—Å—Ç–≤—É—é—Ç —Å–∫–æ–±–∫–∏  d = (5 - 6) - 7')
    d = 5 - 6 - 7
    st.write('5 - 6 - 7 ->', d)
    st.write('-' * 50)
    st.write('–ü–æ —É–º–æ–ª—á–∞–Ω–∏—é –ª–µ–≤–∞—è –∞—Å—Å–æ—Ü–∏–∞—Ç–∏–≤–Ω–æ—Å—Ç—å.')
    st.write('–ö–∞–∫ –±—É–¥—Ç–æ –ø—Ä–∏—Å—É—Ç—Å—Ç–≤—É—é—Ç —Å–∫–æ–±–∫–∏  d = (0 and 0) or 1')
    d = 0 and 0 or 1
    st.write('0 and 0 or 1 ->', d)
    st.write('-' * 50)
    st.write('–¢–µ–ø–µ—Ä—å –ø—Ä–∞–≤–∞—è –∞—Å—Å–æ—Ü–∏–∞—Ç–∏–≤–Ω–æ—Å—Ç—å.')
    st.write('–¢–∞–∫ –∫–∞–∫ –æ–ø–µ—Ä–∞—Ü–∏—è –≤ —Å–∫–æ–±–∫–∞—Ö –≤—ã–ø–æ–ª–Ω—è–µ—Ç—Å—è –ø–µ—Ä–≤–æ–π.')
    d = 0 and (0 or 1)
    st.write('0 and (0 or 1) ->', d)
    st.write('üé• –°–º–æ—Ç—Ä–∏—Ç–µ –º—É–ª—å—Ç—Ñ–∏–ª—å–º: –ø—Ä–æ –ø—Ä–æ–≥—Ä–∞–º–º–∏—Å—Ç–∞')

