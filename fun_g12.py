def run_12_4():
    st.markdown('#### üêç –£—Ä–æ–∫ 12.4. –û–¥–Ω–æ—Å—Ç—Ä–æ—á–Ω–æ–µ –≤—ã—Ä–∞–∂–µ–Ω–∏–µ if/else')
    st.markdown('##### –†–∞–∑–≤–µ—Ç–≤–ª–µ–Ω–∏–µ –≤ –ø—Ä–æ–≥—Ä–∞–º–º–µ –æ–¥–Ω–æ–π —Å—Ç—Ä–æ–∫–æ–π')
    x = 15
    y = 10
    # –û–±—ã—á–Ω–∞—è –∏–Ω—Å—Ç—Ä—É–∫—Ü–∏—è if/else
    if x > y:
        s = x + y
    else:
        s = x - y
    st.write('–û–±—ã—á–Ω–∞—è –∏–Ω—Å—Ç—Ä—É–∫—Ü–∏—è if/else: s=', s)
    # –û–¥–Ω–æ—Å—Ç—Ä–æ—á–Ω–æ–µ (—Ç—Ä–µ—Ö–º–µ—Å—Ç–Ω–æ–µ) –≤—ã—Ä–∞–∂–µ–Ω–∏–µ
    s = x + y if x > y else x - y
    st.write('–û–¥–Ω–æ—Å—Ç—Ä–æ—á–Ω–æ–µ –≤—ã—Ä–∞–∂–µ–Ω–∏–µ if/else: s=', s)
    # –û–¥–Ω–æ—Å—Ç—Ä–æ—á–Ω–æ–µ (—Ç—Ä–µ—Ö–º–µ—Å—Ç–Ω–æ–µ) –≤—ã—Ä–∞–∂–µ–Ω–∏–µ –≤ —Å–æ—Å—Ç–∞–≤–µ –¥—Ä—É–≥–æ–π –∏–Ω—Å—Ç—Ä—É–∫—Ü–∏–∏
    s = x + (x + y if x > y else x - y) + y ** 2
    st.write('–û–¥–Ω–æ—Å—Ç—Ä–æ—á–Ω–æ–µ –≤—ã—Ä–∞–∂–µ–Ω–∏–µ if/else –≤ —Å–æ—Å—Ç–∞–≤–µ –¥—Ä—É–≥–æ–π –∏–Ω—Å—Ç—Ä—É–∫—Ü–∏–∏')
    st.write('s =', s)
    st.write('üé• –°–º–æ—Ç—Ä–∏—Ç–µ –º—É–ª—å—Ç—Ñ–∏–ª—å–º: –ø—Ä–æ –∫–ª–∞–≤–∏–∞—Ç—É—Ä—É')

